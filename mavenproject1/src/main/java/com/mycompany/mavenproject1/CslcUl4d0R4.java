    private void btnactionBhaskaraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactionBhaskaraActionPerformed
    Matematica mat2 = new Matematica();
        
        double a, b, c ,delta, x1, x2;
       
       a = Double.parseDouble(bhNum1.getText());
       b = Double.parseDouble(bhNum2.getText());
       c = Double.parseDouble(bhNum3.getText());
       
       delta = mat2.calcularDelta(a, b, c);
       x1 = mat2.calcularX1(a, b, delta);
       x2= mat2.calcularX2(a, b, delta);
  
       
       deltaResult.setText(Double.toString(delta));
       x1Result.setText(Double.toString(x1));
       x2Result.setText(Double.toString(x2));
  
    }//GEN-LAST:event_btnactionBhaskaraActionPerformed

