    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
     
        CadastroDAO cadastroDao = new CadastroDAO();
        
        
        String nome = txtNome.getText().trim();
        String email = txtEmail.getText().trim();
        String telefone = txtTele.getText().trim();
        String tipoUsuario = txtTipo.getText().trim();
        
        if(nome.isEmpty() || email.isEmpty() || telefone.isEmpty() || 
                tipoUsuario.isEmpty()){
            
            JOptionPane.showMessageDialog(this, 
                    "Preencha todos os campos antes de salvar",
                    "Campos obrigatórios",JOptionPane.WARNING_MESSAGE);
            
            return;
        }
        
        Cadastro pessoa = new Cadastro(0, nome, email, telefone, tipoUsuario);
        
        try {
            cadastroDao.criaPessoa(pessoa);
            JOptionPane.showMessageDialog(this, "Usuário salvo com sucesso!"
                    ,"Sucesso",
                    JOptionPane.INFORMATION_MESSAGE);
            
        } catch (SQLException ex) {
            
//            Logger.getLogger(getClass().getName()).log(Level.SEVERE, 
//                    "Erro ao salvar usúario",ex);
            
            JOptionPane.showMessageDialog(this, 
                    "Não fois possível salvar o usúario.","Detalhes: "
                            +ex.getMessage(),JOptionPane.INFORMATION_MESSAGE);
            
        
            
        }     
            
        

    }//GEN-LAST:event_SaveButtonActionPerformed

