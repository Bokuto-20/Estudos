    CadastroDAO cadastroDao = new CadastroDAO();
    DefaultTableModel model = (DefaultTableModel) tabela.getModel();
    model.setRowCount(0);  // limpa tabela antes
    
    try {
        String valor = JOptionPane.showInputDialog(this, "Informe o ID para busca:");
        if (valor == null || valor.trim().isEmpty()) {
            return; // cancelou ou vazio
        }
        
        int idBuscado = Integer.parseInt(valor.trim());
        
        Cadastro resultado = cadastroDao.buscarPessoaPorId(idBuscado);
        
        if (resultado != null) {
            // Atualiza tabela com o resultado
            model.addRow(new Object[]{
                resultado.getId(),
                resultado.getNome(),
                resultado.getEmail(),
                resultado.getTelefone(),
                resultado.getTipo()
            });
            
            // Preenche campos de texto para edição
            txtNome.setText(resultado.getNome());
            txtEmail.setText(resultado.getEmail());
            txtTele.setText(resultado.getTelefone());
            txtTipo.setText(resultado.getTipo());
            
            idAtual = resultado.getId(); // salva o ID atual para alterar ou deletar
            
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum registro encontrado para o ID: " + idBuscado);
            idAtual = -1;
            txtNome.setText("");
            txtEmail.setText("");
            txtTele.setText("");
            txtTipo.setText("");
        }
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID inválido! Insira um número inteiro.");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro ao buscar no banco: " + e.getMessage());
    }
    
